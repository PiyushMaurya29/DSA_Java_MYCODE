package Basics;

public class dataTypes {
    public static void main(String[] args) {
        boolean flag = true;
        int a = 98;
        short b = 16;
        byte c = 47;
        long d = 29;
        float e = 10;
        double f = 2003;
        char ch = 'P';

        System.out.println("The boolean variable is "+flag);
        System.out.println("The integer variable is "+a);
        System.out.println("The short variable is "+b);
        System.out.println("The byte variable is "+c);
        System.out.println("The long variable is "+d);
        System.out.println("The float variable is "+e);
        System.out.println("The double variable is "+f);
        System.out.println("The character variable is "+ch);
    }
}
