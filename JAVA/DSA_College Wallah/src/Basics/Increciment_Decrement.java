package Basics;

public class Increciment_Decrement{
    public static void main(String[] args) {
        int x = 5;
        x++;
        System.out.println(x);
        x--;
        System.out.println(x);
        ++x;
        System.out.println(x);
        --x;
        System.out.println(x);
    }
}
