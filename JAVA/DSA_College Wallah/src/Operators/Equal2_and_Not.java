package Operators;
import java.util.Scanner;
public class Equal2_and_Not {
    public static void main(String[] args) {
        int p = 20 , q = 25;
        System.out.println(p==q); //false
        System.out.println(p!=q); //true
        System.out.println(p<=q); //true
        System.out.println(p<q); //true
        System.out.println(p>q); //false
    }
}
